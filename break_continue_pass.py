# break statement
for i in range(1,11):
    if(i==5):
        break
    print(i,end=" ")


print()
# continue statement
for i in range(1,11):
    if(i==5):
        continue
    print(i,end=" ")


print()
# pass statement
for i in range(1,11):
    if(i==5):
        pass
    print(i,end=" ")